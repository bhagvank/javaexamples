public class SwitchStatement
{
  public static void main(String args[])
  {
    int color = 0;
    switch(color)
    {
      case 0 :
      System.out.println("White");
      break;
      case 1 :
      System.out.println("Yellow");
      break;
      case 2 :
      System.out.println("Green");
      break;
      case 3 :
      System.out.println("Blue");
      break;
      default :
      System.out.println("Black");
    }
  }
}